const dia = prompt("Digite o dia da semana")

switch(dia){
	case "segunda-feira":
		console.log("Hoje é segunda-feira");
		document.write("Hoje é segunda-feira")
		break;
	case "terça-feira":
		console.log("Hoje é terça-feira");
		document.write("Hoje é terça-feira")
		break;
	case "quarta-feira":
		console.log("Hoje é quarta-feira");
		document.write("Hoje é quarta-feira");
		break;
	default:
		console.log("Hoje não e um dia util");
		document.write("Hoje não e um dia util")
}