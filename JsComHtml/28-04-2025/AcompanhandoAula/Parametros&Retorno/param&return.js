//Funções podem :
//Receber um parâmetro ( entradas)
// Retorna valores (saídas)

//Operações basicas : + , - , * , /

function somar (a,b){
	return a + b ;
};


function divisao (a,b){
	return a / b ;
};


function multi (a,b){
	return a * b ;
};


function subt (a,b){
	return a - b ;
};

const resultado01 = somar (2,3);
console.log("soma:" + " " +resultado01);

const resultado02 = divisao (2,3);
console.log("divisâo:" + " " +resultado02);

const resultado03 = multi (2,3);
console.log("multiplicação:" + " " +resultado03);

const resultado04 = subt (2,3);
console.log("subtração:" + " " +resultado04);
